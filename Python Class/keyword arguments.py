# keyword arguments = arguments preceded by an identifier when we pass them to a function
#                     The order od the argumets doesn't matter, unlike positional arguments
#                     Pythons knows the names of the arguments that our functions receives 

def hello(first,middle,last):
    print("Hello "+first+" "+middle+" "+last)
    
hello(last ="Silva",first="Guga",middle="Alex")
 
